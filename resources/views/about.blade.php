    <h5 class="fw-bold mt-3">American rule</h5>
    <p>After the Peace Treaty between Spain and the United States in 1898, the American regime took over Palawan. In
        1901, a Military Government led by Major John Brown was established, and in June 23, 1902, the Civil Government
        of Paragua was established with Major J. Brown as the appointed Governor. Following the implementation of Act
        No. 1363 of the Philippine Commission in 1905, the name of the province was changed from Paragua to Palawan, and
        the capital was transferred from Cuyo to Puerto Princesa.</p>
    <p>However, on May 18, 1942, the Japanese Imperial Forces occupied Palawan during World War II. Garrisons were
        established in Coron, Puerto Princesa, and Iwahig, leading to the formation of guerilla forces in three sectors.
        Capt. Carlos Amores led the Calamianes sector, Dr. Higinio Mendoza, Sr. (Governor elect 1931-1937) led the Main
        Island, and Emilio Tumbaga led Brooke's Point. Palawan was liberated by the Americans in 1945.</p>
    <h5 class="fw-bold mt-3">Palawan Massacre: A Tragic WWII Event</h5>
    <p>The Palawan Massacre was a brutal event that occurred on December 14, 1944, during World War II. The Japanese
        Fourteenth Area Army under the command of General Tomoyuki Yamashita herded 150 prisoners of war in Puerto
        Princesa into three covered trenches and set them on fire using gasoline to prevent their rescue by advancing
        American allies. Prisoners who tried to escape the flames were shot down, while others who attempted to climb
        over a cliff were hunted down and killed. Only 11 soldiers survived, and between 133 to 141 people were killed.
    </p>
