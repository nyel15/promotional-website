    <h5 class="fw-bold mt-3">Spanish rule</h5>
    <p>The Calamianes Islands located in the northern part of Palawan were among the first to be colonized by the
        Spanish, which declared the area a separate province from the Palawan mainland. In the early 17th century,
        Spanish friars attempted to establish missions in several other towns such as Cuyo, Agutaya, Taytay, and
        Cagayancillo. However, they were met with resistance from the Moro communities. By the 18th century, Spain began
        constructing fortified churches to serve as a base for preaching and as a defense against Moro raids in Cuyo,
        Taytay, Linapacan, and Balabac. Many of these structures still exist today, offering a glimpse into Palawan's
        colorful past.</p>
    <p>In 1749, the Sultanate of Borneo ceded southern Palawan to Spain, which then established its control over the
        entire province. Originally, Palawan was a single province named Calamianes, with Taytay as its capital. Later
        on, it was divided into three provinces: Castilla in the northern section with Taytay as capital, Asturias in
        the southern mainland with Puerto Princesa as capital, and Balabac Island with its capital in the town of
        Principe Alfonso.</p>
